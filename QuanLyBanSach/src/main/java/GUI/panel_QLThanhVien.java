    public void loadCB_LoaiTV()
    {
        ArrayList<DTO_LoaiThanhVien> cbb = DAO_LoaiThanhVien.listLTV();
         cbb_LoaiTV.removeAllItems();
        for(DTO_LoaiThanhVien i : cbb)
        {
            cbb_LoaiTV.addItem(i.TenLoaiTV);
        }
    }
    
    public boolean checkFullInfo_LoaiTV()
    {
        JTextField[] ds = new JTextField[]{txt_MaLTV,txt_TenLTV,txt_TienCanDatLTV,txt_PTGG_LTV};
        for(JTextField i : ds)
            if(i.getText().trim().equals(""))
                return false;
        return true;
    }
    
