    public void loadDS_LTV()
    {
        model_LTV = new DefaultTableModel();
        model_LTV.setColumnIdentifiers(tblTitleLTV);
        List<DTO_LoaiThanhVien> lst = DAO_LoaiThanhVien.getDS_LoaiThanhVien();
        for(DTO_LoaiThanhVien i : lst)
        {
            Vector a = new Vector();
            a.add(i.MaLoaiTV);
            a.add(i.TenLoaiTV);
            a.add(i.TienCanDat);
            a.add(i.PhanTramGiamGia);
           model_LTV.addRow(a);
        }
        table_DS_LTV.setModel(model_LTV);
    }
