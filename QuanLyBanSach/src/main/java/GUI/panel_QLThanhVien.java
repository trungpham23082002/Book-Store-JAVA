    private void btn_XoaLTVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_XoaLTVActionPerformed
        // TODO add your handling code here:
        if(table_DS_LTV.getSelectedRow()<0)
            JOptionPane.showMessageDialog(this, "Chưa chọn thành viên nào!","Thông báo",JOptionPane.YES_OPTION);
        else
        {
            if(!daoLTV.isExistLTV(txt_MaLTV.getText().trim()))
            {
                JOptionPane.showMessageDialog(this, "Mã loại thành viên không tồn tại!","Thông báo",JOptionPane.YES_OPTION);
            }
            else
            {
                if(!daoLTV.isLTV_FKofAnotherTable(txt_MaLTV.getText().trim()))
                {
                    if(daoLTV.delLTV(txt_MaLTV.getText().trim()))
                        JOptionPane.showMessageDialog(this, "Xóa loại thành viên thành công!","Thông báo",JOptionPane.YES_OPTION);
                    else
                        JOptionPane.showMessageDialog(this, "Xóa loại thành viên thất bại!","Thông báo",JOptionPane.YES_OPTION);
                }
                else
                    JOptionPane.showMessageDialog(this, "Loại thành viên đang giữ khóa ngoại!","Thông báo",JOptionPane.YES_OPTION);
            
        }}
        loadDS_LTV();
    }//GEN-LAST:event_btn_XoaLTVActionPerformed

