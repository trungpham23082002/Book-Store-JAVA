    private void btn_KhoaMoTKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_KhoaMoTKActionPerformed
        // TODO add your handling code here:
        if(table_DSTK.getSelectedRow()>0)
        {
            if(!quyen.equals("admin"))
            {
                if(flag)
                {
                    if(_dao.updateTrangThai(id, flag))
                        JOptionPane.showMessageDialog(this, "Mở khóa tài khoản thành công");
                    else
                        JOptionPane.showMessageDialog(this, "Mở khóa tài khoản thất bại");
                }
                else
                {
                    if(_dao.updateTrangThai(id, flag))
                        JOptionPane.showMessageDialog(this, "Khóa tài khoản thành công");
                    else
                        JOptionPane.showMessageDialog(this, "Khóa tài khoản thất bại");
                }
            }
            else
                JOptionPane.showMessageDialog(this, "Không được khóa tài khoản Admin");
            loadDS();
        }
        else
            JOptionPane.showMessageDialog(this, "Vui lòng chọn tài khoản để thao tác");
    }//GEN-LAST:event_btn_KhoaMoTKActionPerformed

