    public void loadDS()
    {
        modelDS =new DefaultTableModel();
        modelDS.setColumnIdentifiers(tblTitle);
       List<DTO_TaiKhoan> lst = _dao.getDS_TaiKhoan();
        for(DTO_TaiKhoan i : lst)
        {
            Vector a = new Vector();
            a.add(i.MaTaiKhoan);
            a.add(i.TenDangNhap);
            a.add(i.MatKhau);
            a.add(i.HoTen);
            a.add(i.SDT);
            a.add(i.MaQuyen);
            a.add(i.BiKhoa);
            modelDS.addRow(a);
        }
        table_DSTK.setModel(modelDS);
    }
