    private void btn_SuaTKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SuaTKActionPerformed
        // TODO add your handling code here:
        if(checkFullInfo_LoaiTV()){
            boolean bikhoa=false; 
            if(_dao.isExistTK(txt_MaTK.getText().trim()))
             {
                    if(!_dao.isExistTenDN(txt_TenDN.getText().trim())||_dao.getTenDNfromMaTK(txt_MaTK.getText().trim()).equals(txt_TenDN.getText().trim()))
                    {
                        String flag = (cbb_Quyen.getSelectedItem().toString().equals("Nhân viên"))?"user":"admin";
                        DTO_TaiKhoan tk =new DTO_TaiKhoan(
                                txt_MaTK.getText().trim(), txt_TenDN.getText(),txt_MK.getText(),txt_HoTen.getText(),txt_SDT.getText(),flag,bikhoa);
                            if(_dao.updateTaiKhoan(tk))
                                JOptionPane.showMessageDialog(this, "Sửa thông tin thành công!","Thông báo",JOptionPane.YES_OPTION);
                            else
                                JOptionPane.showMessageDialog(this, "Sửa thông tin thất bại!","Thông báo",JOptionPane.YES_OPTION);
                    }
                    else
                    JOptionPane.showMessageDialog(this, "Tên đăng nhập đã tồn tại!","Thông báo",JOptionPane.YES_OPTION);
            
            }
            else
                JOptionPane.showMessageDialog(this, "Mã tài khoản sửa không tồn tại!","Thông báo",JOptionPane.YES_OPTION);
        }
        else
            JOptionPane.showMessageDialog(this, "Chưa điền đủ thông tin!","Thông báo",JOptionPane.YES_OPTION);
        loadDS();
    }//GEN-LAST:event_btn_SuaTKActionPerformed

