    private void table_QLSPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_QLSPMouseClicked
        // TODO add your handling code here:
        clearBackgroundColor();
        Point p = evt.getPoint();
        int row = table_QLSP.rowAtPoint(p);
        String maTang = (String)modelDS.getValueAt(row, 8).toString().trim();
        String maKe = (String)modelDS.getValueAt(row, 9).toString().trim();
        
        txt_Tang_TimKiem.setText(_dao.getTenTang(maTang.trim()));
        txt_Ke_TimKiem.setText(_dao.getTenKe(maKe.trim()));
        setBackgroundColor_Ke(maKe);
        setBackgroundColor_Tang(maTang);
    }//GEN-LAST:event_table_QLSPMouseClicked

