   public void loadDS()
    {
       data = new Vector();
       modelDS = new DefaultTableModel();
       modelDS.setColumnIdentifiers(tblTitle);
       List<DTO_SanPham> lst = _dao.getDS_Sach();
        for(DTO_SanPham i : lst)
        {
            Vector a = new Vector();
            a.add(i.MaSach);
            a.add(i.TenSach);
            a.add(i.TacGia);
            a.add(i.SoTrang);
            a.add(i.GiaBan);
            a.add(i.SoLuongTon);
            a.add(i.MaTheLoai);
            a.add(i.MaNXB);
            a.add(i.MaTang);
            a.add(i.MaKe);
            data.add(a);
            modelDS.addRow(a);
        }
        
        table_QLSP.setModel(new DefaultTableModel( data,tblTitle));
    }
