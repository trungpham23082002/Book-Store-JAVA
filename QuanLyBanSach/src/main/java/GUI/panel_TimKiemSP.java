    private void setBackgroundColor_Tang(String _ma)
    {
        if(_ma.equals("T01"))
            txt_Tang1.setBackground(Color.cyan);
        if(_ma.equals("T02"))
            txt_Tang2.setBackground(Color.cyan);
        if(_ma.equals("T03"))
            txt_Tang3.setBackground(Color.cyan);
        if(_ma.equals("T04"))
            txt_Tang4.setBackground(Color.cyan);
    }
    
    private void setBackgroundColor_Ke(String _ma)
    {
        if(_ma.equals("K01"))
            txt_KeA.setBackground(Color.orange);
        if(_ma.equals("K02"))
            txt_KeB.setBackground(Color.orange);
        if(_ma.equals("K03"))
            txt_KeC.setBackground(Color.orange);
        if(_ma.equals("K04"))
            txt_KeD.setBackground(Color.orange);
        if(_ma.equals("K05"))
            txt_KeE.setBackground(Color.orange);
    }
    
    private void clearBackgroundColor()
    {
        JTextField[] ds = new JTextField[]{txt_KeA,txt_KeC,txt_KeB,txt_KeD,txt_KeE,txt_Tang1,txt_Tang2,txt_Tang3,txt_Tang4};
        for(JTextField i : ds)
            i.setBackground(Color.white);
    }

