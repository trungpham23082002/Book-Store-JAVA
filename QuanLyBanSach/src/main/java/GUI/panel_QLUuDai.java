    private void btn_ThemGGActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ThemGGActionPerformed
        // TODO add your handling code here:
        if(checkFullInformation())
        {
            if(Integer.parseInt(txt_PTGG.getText().trim())>0 && Integer.parseInt(txt_PTGG.getText().trim())<100)
            {
                SimpleDateFormat sdf = new SimpleDateFormat("MM-dd-yyyy");
                try { 
                    Date dateBD = sdf.parse(txt_NgayBD.getText().trim());
                    Date dateKT = sdf.parse(txt_NgayKT.getText().trim()); 
                    if(dateBD.compareTo(dateKT)<0)
                    {
                        if(!_dao.checkExistPrimarykey(txt_MaGG.getText().trim()))
                        {
                            VisibleButton_Luu(true, btn_ThemGG);
                        }
                        else
                            JOptionPane.showMessageDialog(this, "Mã giảm giá đã tồn tại!","Thông báo",JOptionPane.YES_OPTION);
                    }
                    else
                            JOptionPane.showMessageDialog(this, "Ngày bắt đầu phải nhỏ hơn ngày kết thúc!","Thông báo",JOptionPane.YES_OPTION);

                } catch (ParseException ex) {
                      JOptionPane.showMessageDialog(this, "Ngày bắt đầu hoặc ngày kết thúc không hợp lệ!","Thông báo",JOptionPane.YES_OPTION);
                }
                
                
            }
            else
            JOptionPane.showMessageDialog(this, "Phần trăm giảm giá không hợp lệ!","Thông báo",JOptionPane.YES_OPTION);
        }
        else
            JOptionPane.showMessageDialog(this, "Chưa nhập đầy đủ thông tin!","Thông báo",JOptionPane.YES_OPTION);
    }//GEN-LAST:event_btn_ThemGGActionPerformed

   
