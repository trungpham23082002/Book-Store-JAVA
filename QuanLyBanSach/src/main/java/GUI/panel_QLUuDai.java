    public void loadDS()
    {
        modelDS=new DefaultTableModel();
        modelDS.setColumnIdentifiers(tblTitle);
       List<DTO_PhieuGG> lst = _dao.getDS_PhieuGiamGia();
        for(DTO_PhieuGG i : lst)
        {
            Vector a = new Vector();
            a.add(i.MaGiamGia);
            a.add(i.TenGiamGia);
            a.add(i.PhanTramGiamGia);
            a.add(i.NgayBD);
            a.add(i.NgayKT);
            data.add(a);
            modelDS.addRow(a);
        }
        table_DSGG.setModel(modelDS);
    }
    
